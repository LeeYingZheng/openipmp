        //

        // @param uid - uid is mapped into license's licenseId attribute.  However, because XMLDocument does not have an API to

        // replace attribute value of an element, this parameter is ignored.

        //

        // @param keyHolderModulus is mapped into the grant's key holder's modulus.

        //

        // @param keyHolderExponent is mapped into the grant's key holder's exponent.

        //

        // @param DOI -DOI is mapped into the diReference's identifier.

        //

        // @param X509Certificate - X509Certificate is mapped into otherInfo's X509 certificate.

        //

        // @param CipherValue is mapped into otherInfo's cipher value.

        //

        // @param startDate - startDate is mapped into validityInterval's notBefore.

        //

        // @param endDate - endDate is mapped into validityInterval's notAfter.

	public void build(String uid,String keyHolderModulus,String keyHolderExponent,

                          String DOI,String X509Certificate,String CipherValue,

                          Date startDate,Date endDate) throws Exception

	{

                setString("license.grant.keyHolder.info.dsig:KeyValue.dsig:RSAKeyValue.dsig:Modulus", keyHolderModulus);

                setString("license.grant.keyHolder.info.dsig:KeyValue.dsig:RSAKeyValue.dsig:Exponent", keyHolderExponent);

		setString("license.grant.mx:diReference.mx:identifier",DOI);



		setString("license.grant.validityInterval.notBefore",convertDate(startDate,true));

		setString("license.grant.validityInterval.notAfter",convertDate(endDate,true));

                setString("license.otherInfo.enc:EncryptedData.dsig:KeyInfo.enc:EncryptedKey.dsig:X509Data.dsig:X509Certificate", X509Certificate);

                setString("license.otherInfo.enc:EncryptedData.dsig:KeyInfo.enc:EncryptedKey.enc:CipherData.enc:CipherValue", CipherValue);

		signDocument();

	}

	public void signDocument()throws Exception

	{

		CertificateAuthority ca = osmsGlobalObjectManager.getInstance().getCerfiticateAuthority();

		//-------------------

		//build license xml without issuer for signing

		//-------------------

		XMLDocument doc = this.CloneXrML();

		doc.remove("license.issuer");

		String license = doc.encode(false);



		//----------

		//set values

		//----------

		setString("license.issuer.dsig:Signature.dsig:SignedInfo.dsig:Reference.dsig:DigestValue", CryptoTool.base64Encode(CryptoTool.generateSHA1Hash(license)));

		setString("license.issuer.dsig:Signature.keyHolder.info.dsig:X509Data.dsig:X509Certificate", CryptoTool.base64Encode(ca.getCaCertificate().getEncoded()));

		setString("license.issuer.dsig:Signature.dsig:SignatureValue", CryptoTool.base64Encode(ca.generateSignedSHA1Digest(license.getBytes())));



	}



